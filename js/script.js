'use strict';

// modal variables
const modal = document.querySelector('[data-modal]');
const modalCloseBtn = document.querySelector('[data-modal-close]');
const modalCloseOverlay = document.querySelector('[data-modal-overlay]');

// modal function
const modalCloseFunc = function () { modal.classList.add('closed') }

// modal eventListener
modalCloseOverlay.addEventListener('click', modalCloseFunc);
modalCloseBtn.addEventListener('click', modalCloseFunc);





// notification toast variables
const notificationToast = document.querySelector('[data-toast]');
const toastCloseBtn = document.querySelector('[data-toast-close]');

// notification toast eventListener
toastCloseBtn.addEventListener('click', function () {
  notificationToast.classList.add('closed');
});



/**
 * add event on element
 */

const addEventOnElem = function (elem, type, callback) {
  if (elem.length > 1) {
    for (let i = 0; i < elem.length; i++) {
      elem[i].addEventListener(type, callback);
    }
  } else {
    elem.addEventListener(type, callback);
  }
}
/**
 * navbar toggle
 */

const navToggler = document.querySelector("[data-nav-toggler]");
const navbar = document.querySelector("[data-navbar]");
const navbarLinks = document.querySelectorAll("[data-nav-link]");

const toggleNavbar = function () {
  navbar.classList.toggle("active");
  navToggler.classList.toggle("active");
}

addEventOnElem(navToggler, "click", toggleNavbar);

const closeNavbar = function () {
  navbar.classList.remove("active");
  navToggler.classList.remove("active");
}

addEventOnElem(navbarLinks, "click", closeNavbar);

/**
 * active header when window scroll down to 100px
 */

const header = document.querySelector("[data-header]");
const backTopBtn = document.querySelector("[data-back-top-btn]");

const activeElemOnScroll = function () {
  if (window.scrollY > 100) {
    header.classList.add("active");
    backTopBtn.classList.add("active");
  } else {
    header.classList.remove("active");
    backTopBtn.classList.remove("active");
  }
}

addEventOnElem(window, "scroll", activeElemOnScroll);

window.addEventListener('DOMContentLoaded', function () {
  const scrollWrapper = document.querySelector('.offer-scroll-wrapper');
  const gridList = document.querySelector('.grid-list');
  if (!scrollWrapper || !gridList) return;

  // 1. Clone all items to make infinite loop effect
  const clonedItems = Array.from(gridList.children).map(child => child.cloneNode(true));
  clonedItems.forEach(child => gridList.appendChild(child));

  // 2. Auto-scroll logic
  const scrollStep = 1;
  const scrollSpeed = 10;

  function autoScrollRight() {
    scrollWrapper.scrollLeft += scrollStep;

    // Infinite seamless scroll â€” loop back after full list
    if (scrollWrapper.scrollLeft >= gridList.scrollWidth) {
      scrollWrapper.scrollLeft -= gridList.scrollWidth / 2;
    }
  }

  // 3. Start scrolling
  setInterval(autoScrollRight, scrollSpeed);
});

